
public class DemoASCII {

	//Write a program to print the characters corresponding to ASCII codes from 65 to 90.

	 
	
	
	public static void main(String args[])
	{
		for (int i = 65; i <= 90; i++) {
		       //upper case A-Z
		        System.out.println(i+" -  "+(char)i);
		    }
	}
}
